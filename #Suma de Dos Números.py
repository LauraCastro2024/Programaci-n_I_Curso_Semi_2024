# Este código suma dos números
# Definimos la variable que toma dos argumentos (a, b). 
# Esto permite que se pueda utilizar con diferentes números.
# Sumamos los dos números y almacenamos el resultado en una tercera variable.
# La palabra clave "return" indica que devolverá el resultado de la suma al lugar donde es llamada.
# "return" realiza la suma y devuelve el resultado. 
def sumar(a, b):
    return a + b 
#El resultado de esta suma se almacena en la variable "resultado".
resultado = sumar(475, 863) 

print("El resultado de la suma es:", resultado) # Imprime el resultado de la suma y mostra el mensaje 
#junto con el valor calculado.
